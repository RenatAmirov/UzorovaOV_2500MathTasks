-- У слона пульс 20 ударов в минуту, а у
-- паука на 40 ударов в минуту больше. Какой
-- пульс у паука? Сравни пульс паука и человека.

-- Объяснение кода:

-- elephantPulse – константа, определяющая пульс слона (20 ударов в минуту).
-- spiderPulse – вычисляемое значение пульса паука, которое равно значению elephantPulse плюс 40.
-- humanPulse – примерное значение пульса человека (в данном случае 60 ударов в минуту). Это значение может варьироваться, но мы используем его для сравнения.
-- В функции main выводим результат вычисления пульса паука и сравниваем его с пульсом человека. Если пульс паука выше, то программа выводит соответствующее сообщение, иначе – другое сообщение.

module Main where

-- Пульс слона
elephantPulse :: Int
elephantPulse = 20

-- Пульс паука
spiderPulse :: Int
spiderPulse = elephantPulse + 40

-- Пульс человека (примерный диапазон)
humanPulse :: Int
humanPulse = 60

main :: IO ()
main = do
  putStrLn $ "Пульс паука: " ++ show spiderPulse
  if spiderPulse > humanPulse then
    putStrLn "Пульс паука выше, чем у человека."
  else
    putStrLn "Пульс паука ниже, чем у человека."